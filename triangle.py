def TriangleArea(a, h):
    """Принимает числа: а - сторона треугольника, h - высота треугольника, проведенная к стороне a;
        Возвращает площадь треугольника - произведение числа а на половину числа h"""
    return a * h / 2

def TrianglePerimeter(a, b, c):
    """Принимает числа а, b, c - стороны треугольника;
        Возвращает периметр треугольника - сумму a, b, c"""
    return a + b + c
